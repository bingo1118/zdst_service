package com.cloudfire.entity;

public class PlanPoint {
	private String mac;
	private float macX;
	private float macY;
	private String left;
	private String top;
	
	public String getLeft() {
		return left;
	}
	public void setLeft(String left) {
		this.left = left;
	}
	public String getTop() {
		return top;
	}
	public void setTop(String top) {
		this.top = top;
	}
	public String getMac() {
		return mac;
	}
	public void setMac(String mac) {
		this.mac = mac;
	}
	public float getMacX() {
		return macX;
	}
	public void setMacX(float macX) {
		this.macX = macX;
	}
	public float getMacY() {
		return macY;
	}
	public void setMacY(float macY) {
		this.macY = macY;
	}
}
