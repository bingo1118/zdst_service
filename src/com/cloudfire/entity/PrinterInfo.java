package com.cloudfire.entity;

public class PrinterInfo {
	private String equipmentDesc;
	private String equipmentFaultInfo;
	private String equipmentFaultType;
	private String mac;
	private String repeater;
	private String updateTime;
	
	public String getEquipmentDesc() {
		return equipmentDesc;
	}
	public void setEquipmentDesc(String equipmentDesc) {
		this.equipmentDesc = equipmentDesc;
	}
	public String getEquipmentFaultInfo() {
		return equipmentFaultInfo;
	}
	public void setEquipmentFaultInfo(String equipmentFaultInfo) {
		this.equipmentFaultInfo = equipmentFaultInfo;
	}
	public String getEquipmentFaultType() {
		return equipmentFaultType;
	}
	public void setEquipmentFaultType(String equipmentFaultType) {
		this.equipmentFaultType = equipmentFaultType;
	}
	public String getMac() {
		return mac;
	}
	public void setMac(String mac) {
		this.mac = mac;
	}
	public String getRepeater() {
		return repeater;
	}
	public void setRepeater(String repeater) {
		this.repeater = repeater;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	
	
}
